function mostrarCarta(){
    var imagen = document.getElementById("fondo");
    if(imagen.src.match("on")){
        imagen.src= "../img/f_de_cartas.jpg";
    }else{
        imagen.src="../img/Héroe_elemental_neos.jpg";
    }
}

